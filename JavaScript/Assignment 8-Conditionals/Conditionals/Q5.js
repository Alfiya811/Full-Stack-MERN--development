/*Q5. Use the while loop to print numbers from 1 to 25 in ascending and descending order*/
//Ascending
console.log("Ascending order: ");
let num1 = 1;
let num2 = 25;
for(i=num1; i<=num2;i++)
{
    console.log(i);
    
}
//Descending
console.log("Descending order: ");
for(i=num2; i>=num1;i--)
{
    console.log(i);
    
}
